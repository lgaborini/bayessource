#' @section Wishart parametrization:
#' 
#' Uses Press/Anderson[1,2] parametrization.
#' 
#' \deqn{X ~ W(v, S)}
#' with \eqn{S = pxp} matrix, \eqn{v >= p} (the degrees of freedom).
#' 
#' Then:
#' \deqn{E[X] = v * S}
#' 
#' @references [1]S. J. Press, Applied multivariate analysis: using Bayesian and frequentist methods of inference. Courier Corporation, 2012.
#' @references [2]T. W. Anderson, An introduction to multivariate statistical analysis, 3rd ed. Hoboken, N.J: Wiley-Interscience, 2003.
